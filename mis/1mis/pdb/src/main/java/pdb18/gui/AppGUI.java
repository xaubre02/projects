/**
 * A class that extends thread that is to be called when program is exiting
 * 
 * @author Tomas Aubrecht (xaubre02@stud.fit.vutbr.cz)
 */
class ExitOfAppGUI extends Thread {

    @Override
    public void run() {
        OracleDBConnection.disconnect();
   }
}

