    /**
     * Switch between Login panel and Main application panel.
     * 
     * @param flag logged status flag
     */
    public void setLogged(boolean flag) {
        if (flag) {
            setContentPane(new MainPanel());
            validate();
        } else {
            setContentPane(new LoginPanel());
            validate();
        }
    }
    
